// Таблица декомпозиции для anchor-matching в TextPerceptor.
//
// Двухуровневое покрытие:
//   1. char_signals — символьный уровень (цифры, операторы, пунктуация).
//   2. word_signals — словарный уровень (русские и английские термины).
//
// Каждая запись: (anchor_id, weight) — id из config/anchors/, вес 0..1.
// Источники: Mathematics_V1_0.md, Writing_V1_0.md, config/anchors/mathematics/primitives.yaml,
//            config/anchors/writing/primitives.yaml.

/// Символы → якорные ID с весами.
/// Возвращает статический срез пар (anchor_id, weight).
pub fn char_signals(c: char) -> &'static [(&'static str, f32)] {
    match c {
        // ── Цифры → math_element ───────────────────────────────────────────────
        '0'..='9' => &[("math_element", 0.8)],

        // ── Математические операторы → math_operation ─────────────────────────
        '+' | '−' | '×' | '·' => &[("math_operation", 0.9), ("prim_hline", 0.2)],
        '-' => &[("math_operation", 0.7), ("prim_hline", 0.4)],
        '*' => &[("math_operation", 0.8)],

        // ── Знаки отношения → math_relation ───────────────────────────────────
        '=' | '≠' | '≈' | '≡' | '<' | '>' | '≤' | '≥' => {
            &[("math_relation", 0.9), ("prim_hline", 0.3)]
        }

        // ── Степени и спецсимволы математики → math_function / math_limit ─────
        '^' | '²' | '³' | '¹' | '⁰' => &[("math_function", 0.8)],
        '∞' | '∂' | '∑' | '∏' | '√' | '∫' | 'π' | 'Σ' => &[("math_limit", 0.95)],
        '/' | '÷' => &[("math_operation", 0.7), ("prim_dslash", 0.4)],

        // ── Греческие буквы (часто в математике) → math_function ──────────────
        'α' | 'β' | 'γ' | 'δ' | 'ε' | 'ζ' | 'η' | 'θ' | 'ι' | 'κ' | 'λ' | 'μ'
        | 'ν' | 'ξ' | 'ο' | 'ρ' | 'σ' | 'τ' | 'υ' | 'φ' | 'χ' | 'ψ' | 'ω' => {
            &[("math_function", 0.7)]
        }

        // ── Скобки → writing (структурные элементы текста) ────────────────────
        '(' | ')' => &[("prim_arc", 0.4)],
        '[' | ']' => &[("prim_vline", 0.35)],
        '{' | '}' => &[("prim_hook", 0.35)],

        // ── Пунктуация → слабый сигнал письма ─────────────────────────────────
        '.' | ',' | ';' => &[("prim_dot", 0.25)],
        ':' => &[("prim_dot", 0.2), ("prim_vline", 0.2)],
        '!' | '?' => &[("prim_hook", 0.3)],
        '—' | '–' | '_' => &[("prim_hline", 0.35)],
        '"' | '\'' | '«' | '»' => &[("prim_arc", 0.3)],
        '…' => &[("prim_dot", 0.3)],

        // ── Вертикальная черта → prim_vline ───────────────────────────────────
        '|' => &[("prim_vline", 0.5)],

        // ── Обратная косая → prim_bslash ──────────────────────────────────────
        '\\' => &[("prim_bslash", 0.5)],

        // ── Прямая косая → math или writing ───────────────────────────────────
        // handled above as '/'

        _ => &[],
    }
}

/// Слова (нижний регистр) → якорные ID с весами.
/// Покрывает русские и английские термины из основных подсистем.
pub fn word_signals(word: &str) -> &'static [(&'static str, f32)] {
    match word {
        // ═══════════════════════════ MATHEMATICS ═════════════════════════════
        // math_element — атомарные объекты
        "элемент" | "element" | "объект" | "число" | "цифра" | "digit" | "integer" => {
            &[("math_element", 1.0)]
        }

        // math_function — отображения, зависимости
        "функция" | "function" | "отображение" | "map" | "производная" | "интеграл"
        | "integral" | "derivative" | "дифференциал" | "differential" | "уравнение"
        | "преобразование" | "transformation" | "log" | "логарифм" | "логарифма"
        | "экспонента" | "синус" | "косинус" | "тангенс" => &[("math_function", 1.0)],

        // math_relation — отношения, равенства
        "отношение" | "relation" | "равно" | "равна" | "равен" | "равенство"
        | "равносильно" | "эквивалентность" | "equivalence" | "изоморфизм"
        | "соответствие" | "упорядоченность" | "порядок" => &[("math_relation", 1.0)],

        // math_operation — действия, операции
        "операция" | "operation" | "действие" | "оператор" | "operator" | "сложение"
        | "вычитание" | "умножение" | "деление" | "возведение" | "корень"
        | "вычислить" | "вычисление" | "calculation" | "compute" => {
            &[("math_operation", 1.0)]
        }

        // math_limit — пределы, бесконечности, асимптоты
        "предел" | "limit" | "граница" | "бесконечность" | "infinity" | "асимптота"
        | "последовательность" | "sequence" | "сходимость" | "convergence"
        | "иррационально" | "иррациональный" | "трансцендентный" => {
            &[("math_limit", 1.0)]
        }

        // math_group — алгебраические структуры
        "группа" | "group" | "кольцо" | "ring" | "алгебра" | "algebra" | "структура"
        | "теорема" | "theorem" | "лемма" | "lemma" | "доказательство" | "proof"
        | "аксиома" | "axiom" | "постулат" | "гипотеза" | "hypothesis" | "гипотезу"
        | "математика" | "mathematics" | "геометрия" | "geometry" | "пифагора" => {
            &[("math_group", 1.0)]
        }

        // math_fractal — рекурсивные/самоподобные структуры
        "фрактал" | "fractal" | "рекурсия" | "recursion" | "итерация" | "iteration"
        | "самоподобие" | "паттерн" | "pattern" | "симметрия" | "symmetry"
        | "масштаб" | "scale" => &[("math_fractal", 0.9)],

        // ═══════════════════════════ WRITING ═════════════════════════════════
        // prim_dot — точечные, атомарные единицы текста
        "точка" | "dot" | "слово" | "word" | "буква" | "letter" | "символ" | "symbol"
        | "знак" | "character" => &[("prim_dot", 1.0)],

        // prim_vline — вертикальные, повествовательные структуры
        "писатель" | "writer" | "автор" | "author" | "поэт" | "poet" | "рассказ"
        | "story" | "текст" | "text" | "написал" | "написала" | "написано" | "пишет"
        | "городе" | "город" | "жил" | "живёт" | "молодой" | "молодая" => {
            &[("prim_vline", 0.9)]
        }

        // prim_hline — горизонтальные линии, размеренный нарратив
        "роман" | "novel" | "повесть" | "строка" | "line" | "абзац" | "paragraph"
        | "глава" | "chapter" | "горизонт" | "horizon" | "дорога" | "путь"
        | "стихотворение" | "стихи" | "poem" | "поэзия" | "poetry" => {
            &[("prim_hline", 0.9)]
        }

        // prim_dslash — динамика, движение в тексте
        "движение" | "motion" | "парус" | "sail" | "волна" | "wave"
        | "ищет" | "мчится" | "летит" | "идёт" => &[("prim_dslash", 0.8)],

        // prim_hook — изгибы, повороты повествования
        "вопрос" | "question" | "восклицание" | "изгиб" | "поворот" | "крюк"
        | "сюжет" | "plot" => &[("prim_hook", 0.8)],

        // prim_arc — плавные переходы, метафоры
        "дуга" | "arc" | "метафора" | "metaphor" | "образ" | "image" | "сравнение"
        | "море" | "sea" | "небо" | "sky" | "туман" | "fog" | "голубом" | "далёкой" => {
            &[("prim_arc", 0.8)]
        }

        _ => &[],
    }
}
