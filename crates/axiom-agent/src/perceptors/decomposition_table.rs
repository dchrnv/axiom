// Двухуровневое покрытие:
//   1. char_signals — символьный уровень (цифры, операторы, пунктуация).
//   2. word_signals — словарный уровень (русские и английские термины).
//
// Каждая запись: (anchor_id, weight) — id из config/anchors/, вес 0..1.
// Источники: Mathematics_V1_0.md, Writing_V1_0.md, config/anchors/mathematics/primitives.yaml,
//            config/anchors/writing/primitives.yaml, config/anchors/logic/primitives.yaml,
